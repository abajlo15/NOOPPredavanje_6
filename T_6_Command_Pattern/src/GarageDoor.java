public class GarageDoor {

	public void up() {

		System.out.println("Openinig garage door...");
	}

	public void stop() {
		System.out.println("Stop the garage door!");

	}

	public void lightOn() {
		System.out.println("Garage lights id on...");
	}

	public void lightOff() {

		System.out.println("Shutting down garage lights...");
	}

	public void down() {
		System.out.println("Closing the garage door...");
	}

}
